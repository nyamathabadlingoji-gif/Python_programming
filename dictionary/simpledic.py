
# This is a simple dictionary in Python. A dictionary is a collection of key-value pairs, where each key is unique and maps to a specific value. In this example, the dictionary contains three key-value pairs: "name" maps to "john", "age" maps to 30, and "course" maps to "python". The `print(dict)` statement will output the entire dictionary when the code is executed.

dict =  {
    "name": "john",
    "age": 30,
    "course": "python"
}
print(dict)

